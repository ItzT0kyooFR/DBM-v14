  async action(cache) {
    const data = cache.actions[cache.index];

    const category = this.getVariable(
      parseInt(data.storage, 10),
      this.evalMessage(data.varName, cache),
      cache
    );

    const categoryName =
      this.evalMessage(data.categoryName, cache) || undefined;
    const categoryPosition =
      parseInt(this.evalMessage(data.categoryPosition, cache), 10) || undefined;
    const reason = this.evalMessage(data.reason, cache) || undefined;

    const payload = {};
    if (categoryName) payload.name = categoryName;
    if (!isNaN(categoryPosition)) payload.position = categoryPosition;

    try {
      await category.edit(payload, { reason });
    } catch (err) {
      this.displayError(data, cache, err);
    }

    this.callNextAction(cache);
  },

