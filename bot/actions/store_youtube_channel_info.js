  async action(cache) {
    const data = cache.actions[cache.index];
    const { youtubeiClient } = this.getDBM().Audio;
    const url = this.evalMessage(data.url, cache);
    const info = parseInt(data.info, 10);
    const youtube = youtubeiClient;

    try {
      let channelId = url.trim();
      if (url.includes("youtube.com")) {
        const urlObj = new URL(url);
        const path = urlObj.pathname;
        if (path.startsWith("/channel/")) {
          channelId = path.split("/channel/")[1];
        } else {
          try {
            const html = await fetch(url).then((res) => res.text());
            const match = html.match(/"browseId":"(UC[a-zA-Z0-9_-]{22})"/);
            if (match) {
              channelId = match[1];
            }
          } catch (err) {
            this.displayError(data, cache, err);
          }
        }
      }

      let channel;
      try {
        channel = await youtube.getChannel(channelId);
      } catch (err) {
        this.displayError(data, cache, err);
        return this.callNextAction(cache);
      }

      let result;
      switch (info) {
        case 0:
          // channel id
          result = channel.id;
          break;
        case 1:
          // channel url
          result = `https://www.youtube.com/channel/${channel.id}`;
          break;
        case 2:
          // channel name
          result = channel.name;
          break;
        case 3:
          // channel handle
          result = channel.handle;
          break;
        case 4:
          // channel description
          result = channel.description;
          break;
        case 5:
          // channel thumbnail url (last)
          result =
            channel.thumbnails?.[channel.thumbnails.length - 1]?.url ?? "";
          break;
        case 6:
          // channel banner url (last)
          result = channel.banner?.[channel.banner.length - 1]?.url ?? "";
          break;
        case 7:
          // channel verified?
          result = channel.verified ?? false;
          break;
        case 8:
          // channel subscriber count
          result = channel.subscriberCount ?? "0";
          break;
        case 9:
          // channel video count
          result = channel.videoCount ?? "0";
          break;
        case 10:
          // channel videos count
          result = channel.videos?.items?.length ?? 0;
          break;
        case 11:
          // channel shorts count
          result = channel.shorts?.items?.length ?? 0;
          break;
        case 12:
          // channel live count
          result = channel.live?.items?.length ?? 0;
          break;
        case 13:
          // channel playlists count
          result = channel.playlists?.items?.length ?? 0;
          break;
        case 14:
          // channel posts count
          result = channel.posts?.items?.length ?? 0;
          break;
        case 15:
          // channel shelves (array)
          result = channel.shelves ?? [];
          break;
        default:
          result = null;
      }
      this.storeValue(
        result,
        parseInt(data.storage, 10),
        this.evalMessage(data.varName, cache),
        cache
      );
    } catch (err) {
      this.displayError(data, cache, err);
    }
    this.callNextAction(cache);
  },

