  async action(cache) {
    const data = cache.actions[cache.index];
    const client = this.getClient();
    const { ActivityType } = require("discord.js");

    const activities = [
      ActivityType.Playing,
      ActivityType.Streaming,
      ActivityType.Listening,
      ActivityType.Watching,
      ActivityType.Custom,
      ActivityType.Competing,
    ];
    const activity = {
      name: this.evalMessage(data.activityName, cache) || "ᅠ", // Name of the activity
      state: this.evalMessage(data.activityState, cache) || undefined, // State of the activity
      type: parseInt(data.activityType, 10), // Type of the activity
      url: this.evalMessage(data.activityUrl, cache) || undefined, // Twitch / YouTube stream URL
    };

    try {
      await client.user.setActivity({
        name: activity.name,
        // shardId? : number | readonly number[]
        state: activity.state,
        type: activities[activity.type],
        url: activity.url,
      });
    } catch (err) {
      this.displayError(data, cache, err);
    }

    this.callNextAction(cache);
  },

