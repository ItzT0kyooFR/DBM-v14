  init() {
    const { document } = this;
    const t = document.getElementById.bind(document);
    const type = t("activityType"),
      nameW = t("activityNameWrapper"),
      stateW = t("activityStateWrapper"),
      urlW = t("activityUrlWrapper");
    const set = (w, e) => {
      const i = w.querySelector("input");
      w.style.opacity = e ? "1" : "0.5";
      i.disabled = !e;
    };
    const update = () => {
      const v = parseInt(type.value, 10);
      set(nameW, false);
      set(stateW, false);
      set(urlW, false);
      if ([0, 2, 3, 5].includes(v)) {
        set(nameW, true);
        set(stateW, true);
      } else if (v === 1) {
        set(nameW, true);
        set(stateW, true);
        set(urlW, true);
      } else if (v === 4) {
        set(stateW, true);
      }
    };
    type.addEventListener("change", update);
    update();
  },

