  //≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡

  async action(cache) {
    const data = cache.actions[cache.index];
    const message = await this.getMessageFromData(
      data.storage,
      data.varName,
      cache
    );

    const type = data.type;

    let sourceButton = null;
    if (cache.interaction.isButton()) {
      sourceButton = cache.interaction.customId;
    }

    let sourceSelect = null;
    if (
      cache.interaction.isStringSelectMenu() ||
      cache.interaction.isUserSelectMenu() ||
      cache.interaction.isRoleSelectMenu() ||
      cache.interaction.isMentionableSelectMenu() ||
      cache.interaction.isChannelSelectMenu()
    ) {
      sourceSelect = cache.interaction.customId;
    }

    let components = null;

    let searchValue = null;

    const messageId = message.id;
    if (type === "all") {
      components = [];
      this.clearAllTemporaryInteractions(messageId);
    } else if (message?.components) {
      const { ActionRowBuilder } = this.getDBM().DiscordJS;
      const oldComponents = message.components;
      const newComponents = [];

      for (let i = 0; i < oldComponents.length; i++) {
        const compData = oldComponents[i];
        const comps =
          compData instanceof ActionRowBuilder ? compData.toJSON() : compData;
        const newComps = [];
        const BUTTON_TYPE = 2;
        const SELECT_TYPES = new Set([3, 5, 6, 7, 8]);
        for (let j = 0; j < comps.components.length; j++) {
          const comp = comps.components[j];
          let deleted = false;
          const id = comp.custom_id ?? comp.customId;
          switch (type) {
            case "allButtons": {
              if (comp.type !== BUTTON_TYPE || comp.type === "BUTTON")
                newComps.push(comp);
              else deleted = true;
              break;
            }
            case "allSelects": {
              if (!SELECT_TYPES.has(comp.type) || comp.type === "SELECT_MENU")
                newComps.push(comp);
              else deleted = true;
              break;
            }
            case "sourceButton": {
              if (id !== sourceButton) newComps.push(comp);
              else deleted = true;
              break;
            }
            case "sourceSelect": {
              if (id !== sourceSelect) newComps.push(comp);
              else deleted = true;
              break;
            }
            case "findButton":
            case "findSelect": {
              if (searchValue === null)
                searchValue = this.evalMessage(data.searchValue, cache);
              if (id !== searchValue && comp.label !== searchValue)
                newComps.push(comp);
              else deleted = true;
              break;
            }
          }
          if (deleted) {
            this.clearTemporaryInteraction(messageId, id);
          }
        }

        comps.components = newComps;
        if (newComps.length > 0) newComponents.push(comps);
      }
      components = newComponents;
    }

    try {
      if (
        cache.interaction?.message?.id === message?.id &&
        cache.interaction?.update &&
        !cache.interaction?.replied
      ) {
        await cache.interaction.update({ components });
      } else if (message?.edit) {
        await message.edit({ components });
      } else {
        if (message.components) {
          message.components = components;
        }
      }
    } catch (err) {
      this.displayError(data, cache, err);
    }
    this.callNextAction(cache);
  },

