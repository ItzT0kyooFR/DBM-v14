  action(cache) {
    const data = cache.actions[cache.index];
    const varName2 = this.evalMessage(data.varName2, cache);
    const storage2 = parseInt(data.storage2, 10);
    const path = this.evalMessage(data.path, cache);
    const DEBUG = parseInt(data.debugMode, 10);
    let jsonRaw = this.getVariable(
      parseInt(data.storage, 10),
      this.evalMessage(data.varName, cache),
      cache
    );
    let jsonData;
    try {
      jsonData = typeof jsonRaw === "object" ? jsonRaw : JSON.parse(jsonRaw);
    } catch (err) {
      const errorJson = JSON.stringify({
        error: "Invalid JSON source",
        success: false,
      });
      this.storeValue(errorJson, storage2, varName2, cache);
      if (DEBUG) this.displayError(data, cache, err);
      if (data.behavior === "0") this.callNextAction(cache);
      return;
    }
    let outData = null;
    try {
      if (path) {
        if (path.startsWith("$..")) {
          const key = path.replace("$..", "");
          outData = deepFind(jsonData, key);
        } else {
          outData = getByPath(jsonData, path);

          if (outData === undefined && !path.startsWith("$")) {
            outData = getByPath(jsonData, `$.${path}`);
          }
        }
      }
    } catch (err) {
      outData = null;
      if (DEBUG) this.displayError(data, cache, err);
    }
    if (outData === undefined) outData = null;
    if (Array.isArray(outData) && outData.length === 1) {
      outData = outData[0];
    }
    if (DEBUG) {
      console.log("Source JSON:", jsonData);
      console.log("Path:", path);
      console.log("Result:", outData);
    }
    try {
      const outValue = JSON.stringify(outData);
      this.storeValue(outValue, storage2, varName2, cache);
    } catch (err) {
      const errorJson = JSON.stringify({
        error: "Result is not serializable",
        success: false,
      });
      this.storeValue(errorJson, storage2, varName2, cache);
      if (DEBUG) this.displayError(data, cache, err);
    }
    if (data.behavior === "0") {
      this.callNextAction(cache);
    }
  },

