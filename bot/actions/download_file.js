  async action(cache) {
    const data = cache.actions[cache.index];
    const fs = require("fs");
    const https = require("https");
    const url = this.evalMessage(data.url, cache);
    const fileName = this.evalMessage(data.fileName, cache) || "file";
    const fileFormat = this.evalMessage(data.fileFormat, cache) || "txt";
    const filePath = this.evalMessage(data.filePath, cache) || "./downloads";
    const fullPath = `${filePath}/${fileName}.${fileFormat}`;
    await fs.promises.mkdir(filePath, { recursive: true });
    try {
      await fs.promises.access(fullPath);
    } catch {
      await fs.promises.writeFile(fullPath, "");
    }
    await new Promise((resolve, reject) => {
      const ws = fs.createWriteStream(fullPath);
      ws.on("open", () => {
        https
          .get(url, (res) => {
            res.pipe(ws);
            ws.on("finish", resolve);
            ws.on("error", reject);
          })
          .on("error", reject);
      });
    });

    this.callNextAction(cache);
  },

