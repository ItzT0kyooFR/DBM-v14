  async action(cache) {
    const data = cache.actions[cache.index];
    const { ThreadAutoArchiveDuration } = require("discord.js");

    const autoArchiveMap = [
      ThreadAutoArchiveDuration.OneHour,
      ThreadAutoArchiveDuration.OneDay,
      ThreadAutoArchiveDuration.ThreeDays,
      ThreadAutoArchiveDuration.OneWeek,
    ];

    const channel = await this.getChannelFromData(
      data.channel,
      data.varName,
      cache
    );

    const name = this.evalMessage(data.channelName, cache) || undefined;
    const sm = parseInt(this.evalMessage(data.channelSlowmode, cache), 10);
    const slowmode = isNaN(sm) ? undefined : sm;
    const aadRaw = this.evalMessage(data.channelAutoArchiveDuration, cache);
    const aadIndex = parseInt(aadRaw, 10);
    const autoArchiveDuration = isNaN(aadIndex)
      ? undefined
      : autoArchiveMap[aadIndex];
    const reason = this.evalMessage(data.reason, cache) || undefined;
    const payload = {
      name,
      rateLimitPerUser: slowmode,
      autoArchiveDuration,
    };

    try {
      await channel.edit(payload, { reason });
    } catch (error) {
      this.displayError(data, cache, error);
    }
    this.callNextAction(cache);
  },

