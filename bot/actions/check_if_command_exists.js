  action(cache) {
    const data = cache.actions[cache.index];
    const { Files } = this.getDBM();
    let name = this.evalMessage(data.commandName, cache)?.trim();
    if (!name) return this.executeResults(false, data.branch ?? data, cache);
    const tag = Files.data.settings.tag;
    const serverTag = cache.server?.tag;
    if (serverTag && name.startsWith(serverTag)) {
      name = name.slice(serverTag.length).split(" ")[0];
    } else if (tag && name.startsWith(tag)) {
      name = name.slice(tag.length).split(" ")[0];
    }
    const commands = Files.data.commands ?? [];
    const result = commands.some(
      (cmd) =>
        cmd &&
        (cmd.name === name ||
          (Array.isArray(cmd._aliases) && cmd._aliases.includes(name)))
    );
    this.executeResults(result, data.branch ?? data, cache);
  },

