  action(cache) {
    const data = cache.actions[cache.index];
    const text = this.evalMessage(data.textToReplace, cache);
    const oldVal = this.evalMessage(data.text1, cache);
    const newVal = this.evalMessage(data.text2, cache);
    const type = parseInt(data.type, 10);
    let result;
    if (type === 0) {
      result = text.replace(oldVal, newVal);
    } else if (type === 1) {
      const index = text.lastIndexOf(oldVal);
      if (index !== -1) {
        result =
          text.slice(0, index) + newVal + text.slice(index + oldVal.length);
      } else {
        result = text;
      }
    } else {
      result = text.split(oldVal).join(newVal);
    }
    if (result !== undefined) {
      this.storeValue(
        result,
        parseInt(data.storage, 10),
        this.evalMessage(data.varName, cache),
        cache,
      );
    }
    this.callNextAction(cache);
  },

