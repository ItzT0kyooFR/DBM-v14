  async action(cache) {
    const data = cache.actions[cache.index];
    const server = cache.server;
    if (!server) {
      this.callNextAction(cache);
      return;
    }
    const channel = await this.getChannelFromData(
      data.storage,
      data.varName,
      cache,
    );
    const role = await this.getRoleFromData(data.role, data.varName2, cache);
    const reason = this.evalMessage(data.reason, cache) || undefined;
    let changeTo;
    if (data.changeTo === "0") changeTo = true;
    else if (data.changeTo === "1") changeTo = false;
    else if (data.changeTo === "2") changeTo = null;
    const permissions = this.getVariable(
      parseInt(data.perms, 10),
      this.evalMessage(data.varNamePerms, cache),
      cache,
    );
    const options = {};
    if (Array.isArray(permissions)) {
      for (const p of permissions) {
        options[p] = changeTo;
      }
    } else {
      this.displayError(data, cache, "Permission list is not an array");
      this.callNextAction(cache);
      return;
    }

    try {
      await channel.permissionOverwrites.edit(role.id, options, {
        reason,
      });
    } catch (err) {
      this.displayError(data, cache, err);
    }
    this.callNextAction(cache);
  },

