  async action(cache) {
    const data = cache.actions[cache.index];
    const info = parseInt(data.info, 10);
    const search = this.evalMessage(data.search, cache);
    const channel = await this.getChannelFromData(
      data.channel,
      data.varName,
      cache,
    );
    if (!channel) return this.callNextAction(cache);
    if (!search) {
      this.displayError(data, cache, "Please input something to search");
      return this.callNextAction(cache);
    }
    let message;
    try {
      switch (info) {
        case 0:
          const messages = await channel.messages.fetch({ limit: 100 });
          message = messages.find((el) => el.content.includes(search));
          break;
        case 1:
          message = await channel.messages.fetch(search);
          break;
        default:
          break;
      }
    } catch (err) {
      this.displayError(data, cache, err);
    }
    if (message !== undefined) {
      const storage = parseInt(data.storage, 10);
      const varName2 = this.evalMessage(data.varName2, cache);
      this.storeValue(message, storage, varName2, cache);
    }
    this.callNextAction(cache);
  },

