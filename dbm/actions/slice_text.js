  action(cache) {
    const data = cache.actions[cache.index];
    const textToSlice = this.evalMessage(data.textToSlice, cache);
    const startingFrom =
      parseInt(this.evalMessage(data.startingNumber, cache), 10) || 0;
    const sliceLengthRaw = this.evalMessage(data.sliceLength, cache);
    let result;
    if (sliceLengthRaw === "" || sliceLengthRaw == null) {
      result = `${textToSlice}`.slice(startingFrom);
    } else {
      const sliceLength = parseInt(sliceLengthRaw, 10);
      result = `${textToSlice}`.slice(startingFrom, startingFrom + sliceLength);
    }
    this.storeValue(
      result,
      parseInt(data.storage, 10),
      this.evalMessage(data.varName, cache),
      cache,
    );
    this.callNextAction(cache);
  },

