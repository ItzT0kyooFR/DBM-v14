  async action(cache) {
    const data = cache.actions[cache.index];
    const find = this.evalMessage(data.find, cache);
    const type = parseInt(data.type, 10);
    let emojis;
    let result;
    if (data.mode) {
      emojis = this.getClient().emojis.cache;
    } else {
      const server = await this.getServerFromData(
        data.server,
        data.varName,
        cache,
      );
      if (!server) return this.callNextAction(cache);
      emojis = server.emojis.cache;
    }
    switch (type) {
      case 0:
        result = Boolean(emojis.get(find));
        break;
      case 1:
        result = Boolean(emojis.find((c) => c.name === find));
        break;
    }
    this.executeResults(result, data?.branch ?? data, cache);
  },

