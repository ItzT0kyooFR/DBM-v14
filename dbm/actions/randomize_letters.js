  async action(cache) {
    const data = cache.actions[cache.index];
    const options = this.evalMessage(data.options, cache);
    const wordLength = this.evalMessage(data.wordLength, cache);
    const customCharacters = this.evalMessage(data.customCharacters, cache);
    function randomizeLetters(opt, len, custom = "") {
      const sets = {
        a: "abcdefghijklmnopqrstuvwxyz",
        A: "ABCDEFGHIJKLMNOPQRSTUVWXYZ",
        0: "0123456789",
        "!": "~!@#$%^&()_+-={}[];\\',.",
      };
      let chars = "";
      for (let o of opt) {
        if (o == "*") chars += sets.a + sets.A + sets["0"] + sets["!"];
        else if (o == "?") chars += custom;
        else if (sets[o]) chars += sets[o];
      }
      let r = "";
      for (let i = 0; i < len; i++)
        r += chars[Math.floor(Math.random() * chars.length)];
      return r;
    }
    const result = randomizeLetters(options, wordLength, customCharacters);
    this.storeValue(
      result,
      parseInt(data.storage, 10),
      this.evalMessage(data.varName, cache),
      cache,
    );
    this.callNextAction(cache);
  },

