  async action(cache) {
    const data = cache.actions[cache.index];
    const server = cache.server;
    if (!server) return this.callNextAction(cache);

    const varName = this.evalMessage(data.varName, cache);
    const image = this.getVariable(parseInt(data.storage, 10), varName, cache);
    const { Images } = this.getDBM();

    let buffer;
    try {
      buffer = await Images.createBuffer(image, "image/png");
    } catch {
      return this.displayError(data, cache);
    }

    const tag = this.evalMessage(data.tag, cache);
    const description = this.evalMessage(data.description, cache) || undefined;

    try {
      const sticker = await server.stickers.create({
        file: buffer,
        name: this.evalMessage(data.stickerName, cache),
        tags: tag,
        description,
      });
      this.storeValue(
        sticker,
        parseInt(data.storage2, 10),
        this.evalMessage(data.varName2, cache),
        cache,
      );
    } catch (err) {
      this.displayError(data, cache, err);
    }
    this.callNextAction(cache);
  },

