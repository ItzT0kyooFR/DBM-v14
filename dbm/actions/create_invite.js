  async action(cache) {
    const data = cache.actions[cache.index];
    const channel = await this.getChannelFromData(
      data.channel,
      data.varName,
      cache,
    );
    const reason = this.evalMessage(data.reason, cache);

    const options = {};
    if (data.maxUses) {
      options.maxUses = parseInt(this.evalMessage(data.maxUses, cache), 10);
    } else {
      options.maxUses = 0;
    }
    if (data.lifetime) {
      options.maxAge = parseInt(this.evalMessage(data.lifetime, cache), 10);
    } else {
      options.maxAge = 0;
    }
    if (options.maxAge > 86400) options.maxAge = 86400;
    if (reason) options.reason = reason;
    options.temporary = data.temporary === "true";
    options.unique = data.unique === "true";

    try {
      const invite = await channel.createInvite(options);
      const varName2 = this.evalMessage(data.varName2, cache);
      const storage2 = parseInt(data.storage, 10);
      this.storeValue(invite, storage2, varName2, cache);
    } catch (err) {
      this.displayError(data, cache, err);
    }
    this.callNextAction(cache);
  },

