  //≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡

  async action(cache) {
    const data = cache.actions[cache.index];
    const storage = parseInt(data.storage, 10);
    const info = data.info;
    const path = require("path");
    const { existsSync } = require("fs");
    const { stat, readFile } = require("fs/promises");
    const varName = this.evalMessage(data.varName, cache);
    const filePath = this.evalMessage(data.filePath, cache);
    if (!filePath) return this.callNextAction(cache);
    let result;
    switch (info) {
      case "File Size":
        {
          const stats = await stat(filePath);
          result = stats.size;
        }
        break;
      case "File Extension":
        result = path.extname(path.basename(filePath));
        break;
      case "File Character Count":
        const content = await readFile(filePath, "utf8");
        result = content.length;
        break;
      case "File Creation Date Timestamp":
        {
          const stats = await stat(filePath);
          result = stats.mtimeMs;
        }
        break;
      case "File Exists":
        result = existsSync(filePath);
        break;
      case "File Content":
        result = await readFile(filePath, "utf8");
        break;
      case "File Name":
        result = path.basename(filePath);
        break;
      case "File Length":
        {
          const content = await readFile(filePath, "utf8");
          result = content.split(/\r\n|\r|\n/).length;
        }
        break;
    }
    this.storeValue(result, storage, varName, cache);
    this.callNextAction(cache);
  },

