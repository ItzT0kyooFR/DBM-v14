  async action(cache) {
    const data = cache.actions[cache.index];
    const { Bot } = this.getDBM();
    const translate = Bot.require("translate").default;
    translate.engine = "google";
    const translateFrom = this.evalMessage(data.translateFrom, cache) || "en";
    const translateTo = this.evalMessage(data.translateTo, cache) || "en";
    const translateMessage = this.evalMessage(data.translateMessage, cache);
    if (!translateMessage || !translateFrom || !translateTo) {
      return this.callNextAction(cache);
    }
    try {
      const result = await translate(translateMessage, {
        from: translateFrom,
        to: translateTo,
      });
      const storage = parseInt(data.storage, 10);
      const varName = this.evalMessage(data.varName, cache);
      this.storeValue(result, storage, varName, cache);
    } catch (err) {
      this.displayError(data, cache, err);
    }
    this.callNextAction(cache);
  },

