  async action(cache) {
    const data = cache.actions[cache.index];
    const { MessageFlags } = require("discord.js");
    const msg = await this.getMessageFromData(
      data.message,
      data.varName,
      cache,
    );

    const info = parseInt(data.info, 10);
    let result = false;
    switch (info) {
      case 0:
        result = msg.pinnable;
        break;
      case 1:
        result = msg.pinned;
        break;
      case 2:
        result = msg.deletable;
        break;
      case 3:
        result = msg.deleted;
        break;
      case 4:
        result = msg.tts;
        break;
      case 5:
        result = msg.system;
        break;
      case 6:
        result = msg.mentions.everyone;
        break;
      case 7:
        result = msg.content?.includes("@here");
        break;
      case 8:
        result = msg.mentions.users.size > 0;
        break;
      case 9:
        result = msg.mentions.roles.size > 0;
        break;
      case 10:
        result = msg.mentions.channels.size > 0;
        break;
      case 11:
        result = msg.attachments.size > 0;
        break;
      case 12:
        result = msg.embeds.length > 0;
        break;
      case 13:
        result = msg.components.length > 0;
        break;
      case 14:
        result = msg.author?.bot;
        break;
      case 15:
        result = !!msg.reference;
        break;
      case 16:
        result = msg.channel?.isThread();
        break;
      case 17:
        result = msg.flags?.has(MessageFlags.Ephemeral);
        break;
      case 18:
        result = msg.stickers.size > 0;
        break;
    }

    this.executeResults(result, data?.branch ?? data, cache);
  },

