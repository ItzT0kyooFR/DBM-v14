  async action(cache) {
    const data = cache.actions[cache.index];
    const server = cache.server;
    if (!server) return this.callNextAction(cache);
    const roleData = {};
    if (data.roleName) {
      roleData.name = this.evalMessage(data.roleName, cache);
    }
    if (data.primaryColor) {
      roleData.colors = {
        primaryColor: this.evalMessage(data.primaryColor, cache),
      };
    }
    if (data.position) {
      roleData.position = parseInt(this.evalMessage(data.position, cache), 10);
    }
    roleData.hoist = data.hoist === "true";
    roleData.mentionable = data.mentionable === "true";
    roleData.reason = this.evalMessage(data.reason, cache) || undefined;
    const storage = parseInt(data.storage, 10);
    try {
      const role = await server.roles.create(roleData);
      const varName = this.evalMessage(data.varName, cache);
      this.storeValue(role, storage, varName, cache);
    } catch (err) {
      this.displayError(data, cache, err);
    }
    this.callNextAction(cache);
  },

