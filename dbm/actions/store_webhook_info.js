  async action(cache) {
    const data = cache.actions[cache.index];
    const type = parseInt(data.webhook, 10);
    const varName = this.evalMessage(data.varName, cache);
    const info = parseInt(data.info, 10);
    const client = this.getClient();
    let webhook = this.getVariable(type, varName, cache);
    if (!webhook) return this.callNextAction(cache);
    let result;
    try {
      if ([3, 4, 5, 6].includes(info)) {
        webhook = await client.fetchWebhook(webhook.id);
      }
      switch (info) {
        case 0:
          // webhook url
          result = webhook.url;
          break;
        case 1:
          // webhook id
          result = webhook.id;
          break;
        case 2:
          // webhook token
          result = webhook.token;
          break;
        case 3:
          // webhook name
          result = webhook.name;
          break;
        case 4:
          // webhook owner
          result = webhook.owner;
          break;
        case 5:
          // webhook guild
          result = webhook.guild;
          break;
        case 6:
          // webhook channel
          result = webhook.channel;
          break;
        default:
          break;
      }
    } catch (err) {
      this.displayError(data, cache, err);
    }
    if (result !== undefined) {
      const storage = parseInt(data.storage, 10);
      const varName2 = this.evalMessage(data.varName2, cache);
      this.storeValue(result, storage, varName2, cache);
    }
    this.callNextAction(cache);
  },

