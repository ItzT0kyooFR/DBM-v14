  async action(cache) {
    const data = cache.actions[cache.index];
    const { ThreadAutoArchiveDuration } = require("discord.js");

    const autoArchiveMap = [
      ThreadAutoArchiveDuration.OneHour,
      ThreadAutoArchiveDuration.OneDay,
      ThreadAutoArchiveDuration.ThreeDays,
      ThreadAutoArchiveDuration.OneWeek,
    ];

    const channel = await this.getChannelFromData(
      data.channel,
      data.channelVarName,
      cache,
    );
    const msg = await this.getMessageFromData(
      data.storage1,
      data.varName1,
      cache,
    );

    const payload = {
      name: this.evalMessage(data.postName, cache),
      message: msg,
      autoArchiveDuration:
        autoArchiveMap[parseInt(data.autoArchiveDuration, 10) || 0],
      rateLimitPerUser:
        parseInt(this.evalMessage(data.slowMode, cache), 10) || 0,
      reason: this.evalMessage(data.postReason, cache) || undefined,
    };

    try {
      const postChannel = await channel.threads.create(payload);
      const storage = parseInt(data.storage, 10);
      const storageVarName = this.evalMessage(data.storageVarName, cache);
      this.storeValue(postChannel, storage, storageVarName, cache);
      this.callNextAction(cache);
    } catch (err) {
      this.displayError(data, cache, err);
    }
  },

