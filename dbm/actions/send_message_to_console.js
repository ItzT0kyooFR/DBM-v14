  action(cache) {
    const data = cache.actions[cache.index];
    const { Bot } = this.getDBM();
    const { default: chalk } = Bot.require("chalk");

    const message = this.evalMessage(data.message, cache);
    const color = this.evalMessage(data.color, cache);
    const logType = data.logType;

    switch (logType) {
      case "0":
        console.log(chalk.hex(color)(message));
        break;
      case "1":
        console.info(chalk.hex(color)(message));
        break;
      case "2":
        console.warn(chalk.hex(color)(message));
        break;
      case "3":
        console.error(chalk.hex(color)(message));
        break;
      case "4":
        console.debug(chalk.hex(color)(message));
        break;
      default:
        console.log(chalk.hex(color)(message));
    }

    this.callNextAction(cache);
  },

