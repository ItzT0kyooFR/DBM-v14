  async action(cache) {
    const data = cache.actions[cache.index];

    const channel = await this.getChannelFromData(
      data.channel,
      data.varName,
      cache,
    );

    const slowmode = parseInt(
      this.evalMessage(data.channelSlowmode, cache),
      10,
    );
    const position = parseInt(
      this.evalMessage(data.channelPosition, cache),
      10,
    );
    const reason = this.evalMessage(data.reason, cache) || undefined;

    const payload = {
      name: this.evalMessage(data.channelName, cache) || undefined,
      topic: this.evalMessage(data.channelTopic, cache) || undefined,
      position: isNaN(position) ? undefined : position,
      parent: this.evalMessage(data.channelParent, cache) || undefined,
      rateLimitPerUser: isNaN(slowmode) ? undefined : slowmode,
      nsfw: data.channelNsfw,
    };

    try {
      await channel.edit(payload, { reason });
    } catch (err) {
      this.displayError(data, cache, err);
    }

    this.callNextAction(cache);
  },

